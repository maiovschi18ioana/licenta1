DROP  DATABASE IF EXISTS departament;
CREATE DATABASE IF NOT EXISTS departament;
USE `departament` ;

CREATE TABLE IF NOT EXISTS `departament`.`angajati` (
  `idAngajat` INT NOT NULL AUTO_INCREMENT,
  `nume` VARCHAR(45) NOT NULL,
  `prenume` VARCHAR(45) NOT NULL,
  `email` VARCHAR(45) NOT NULL UNIQUE,
  `cnp` VARCHAR(45) NOT NULL UNIQUE,
  `data_angajare` DATE NOT NULL,
  `salariu` INT(11) NULL ,
  `tip_angajat` ENUM("Administrator","Director piloti","Director stewarzi",
  "Director servicii","Pilot","Steward","Serviciul planificari",
  "Serviciul gestiune si analiza operatiuni zboruri","Serviciul suport tehnic zboruri") ,
  `calificari` VARCHAR(45)  NULL,
  `username` VARCHAR(45) NOT NULL UNIQUE,
  `parola` VARCHAR(45) NOT NULL,
  PRIMARY KEY (`idAngajat`));
DESCRIBE angajati;
SELECT *FROM angajati;

CREATE TABLE IF NOT EXISTS `departament`.`avioane` (
  `idAvion` INT(11) NOT NULL AUTO_INCREMENT,
  `model` VARCHAR(45)  NULL,
  `marca` VARCHAR(45)  NULL,
  `nume` VARCHAR(45)  NULL,
  `data_fabricatie` DATE NULL,
  PRIMARY KEY (`idAvion`));
DESCRIBE avioane;
SELECT *FROM avioane;

CREATE TABLE IF NOT EXISTS `departament`.`documente` (
  `idDocument` INT(11) NOT NULL AUTO_INCREMENT,
  `idAngajat` INT(11) NOT NULL,
  `cale` VARCHAR(45) NOT NULL,
  `nume` VARCHAR(45) NOT NULL,
  PRIMARY KEY (`idDocument`),
  CONSTRAINT `fk_ang_doc`
    FOREIGN KEY (`idAngajat`)
    REFERENCES `departament`.`angajati` (`idAngajat`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION);
DESCRIBE documente;
SELECT *FROM documente;

CREATE TABLE IF NOT EXISTS `departament`.`rute` (
  `idRuta` INT(11) NOT NULL AUTO_INCREMENT,
  `aeroport_plecare` VARCHAR(45)  NULL,
  `aeroport_sosire` VARCHAR(45)  NULL,
  PRIMARY KEY (`idRuta`));
DESCRIBE rute;
SELECT *FROM rute;

CREATE TABLE IF NOT EXISTS `departament`.`zboruri` (
  `idZbor` INT(11) NOT NULL AUTO_INCREMENT,
  `idRuta` INT(11)  NULL,
  `idAvion` INT(11)  NULL,
  `nrZbor` VARCHAR(45) NOT NULL UNIQUE,
  `data_ora_plecare` DATETIME NOT NULL,
  `data_ora_sosire` DATETIME NOT NULL,
  `Observatii` VARCHAR(45)  NULL,
  `stareZbor` VARCHAR(45) NOT NULL,
  PRIMARY KEY (`idZbor`),
  CONSTRAINT `avion_fk`
    FOREIGN KEY (`idAvion`)
    REFERENCES `departament`.`avioane` (`idAvion`)
    ON DELETE SET NULL
    ON UPDATE NO ACTION,
  CONSTRAINT `ruta_fk`
    FOREIGN KEY (`idRuta`)
    REFERENCES `departament`.`rute` (`idRuta`)
    ON DELETE SET NULL
    ON UPDATE NO ACTION);
DESCRIBE zboruri;
SELECT *FROM zboruri;

CREATE TABLE IF NOT EXISTS `departament`.`programe` (
  `idProgram` INT(11) NOT NULL AUTO_INCREMENT,
  `tip_activitate` ENUM("DUTY","ZBOR"),
  `idZbor` INT(11) NULL,
  `idAngajat` INT(11) NOT NULL,
  `date` DATE NOT NULL,
  PRIMARY KEY (`idProgram`),
  CONSTRAINT `fk_ang`
    FOREIGN KEY (`idAngajat`)
    REFERENCES `departament`.`angajati` (`idAngajat`)
    ON DELETE CASCADE
    ON UPDATE NO ACTION,
  CONSTRAINT `fk_zbor`
    FOREIGN KEY (`idZbor`)
    REFERENCES `departament`.`zboruri` (`idZbor`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION);
DESCRIBE programe;
SELECT *FROM programe;

