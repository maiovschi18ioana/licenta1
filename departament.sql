SELECT rute.idRuta,aeroport_plecare, aeroport_sosire,numarcurse FROM rute  INNER JOIN (SELECT DISTINCT idRuta, count(*) AS numarcurse FROM zboruri GROUP BY idRuta ) AS numar ON rute.idRuta=numar.idRuta;

SELECT programe.idAngajat, COUNT(*) FROM programe WHERE programe.tip_activitate = "ZBOR" GROUP BY programe.idAngajat;

SELECT programe.idAngajat, angajati.nume,angajati.prenume , COUNT(*) AS nrzboruri, (COUNT(*)*100/(SELECT COUNT(*) FROM zboruri)) AS procentaj FROM programe INNER JOIN angajati ON (angajati.idAngajat=programe.idAngajat) WHERE  programe.tip_activitate="ZBOR" AND angajati.tip_angajat="Steward"  GROUP BY programe.idAngajat,angajati.nume,angajati.prenume;

SELECT programe.idAngajat, angajati.tip_angajat , COUNT(*) as nrzboruri, (COUNT(*)*100/(SELECT COUNT(*) FROM zboruri)) AS procentaj FROM programe INNER JOIN angajati ON (angajati.idAngajat=programe.idAngajat) WHERE programe.tip_activitate="ZBOR" AND angajati.tip_angajat="Pilot"  GROUP BY programe.idAngajat,angajati.tip_angajat;

SELECT programe.idAngajat, angajati.tip_angajat , COUNT(*) AS nrzboruri, (COUNT(*)*100/(SELECT COUNT(*)FROM zboruri)) AS procentaj FROM programe INNER JOIN angajati ON (angajati.idAngajat=programe.idAngajat) WHERE programe.tip_activitate="ZBOR" AND angajati.tip_angajat="Pilot" GROUP BY programe.idAngajat,angajati.tip_angajat;

SELECT zboruri.idAvion,avioane.model,avioane.nume,COUNT(*) AS numarzboruri,(COUNT(*)/(SELECT COUNT(*) FROM zboruri))*100 AS procentaj FROM zboruri JOIN avioane ON(zboruri.idAvion = avioane.idAvion) GROUP BY zboruri.idAvion,avioane.model,avioane.nume ORDER BY COUNT(*) DESC;

SELECT zboruri.idRuta,rute.aeroport_plecare,rute.aeroport_sosire,COUNT(*),(COUNT(*)/(SELECT COUNT(*) FROM zboruri))*100 AS procentaj FROM zboruri JOIN rute ON(zboruri.idRuta = rute.idRuta) GROUP BY zboruri.idRuta,rute.aeroport_plecare,rute.aeroport_sosire ORDER BY COUNT(*) DESC;